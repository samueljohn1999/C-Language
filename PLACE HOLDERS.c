#include<stdio.h>
#include<stdlib.h>

int main()
{
    printf("PLACE HOLDERS\n\n");
    printf("%d\n",77);///INTEGER
    printf("%ld\n",123456789);///%ld - LARGE INTEGER
    printf("%f\n",12.7);///%f - float FOR PRINTING NNUMBERS AFTER DECIMAL POINTS
    printf("%lf\n",12345.6789);///%lf - large float
    printf("%c\n",'s');///%c - char - TO PRINT A SINGLE CHARACTER
    printf("%s\n","HELLO WORLD");///%s- string - TO PRINT A SET OF CHARESTERS TOGETHER
    printf("%x\n",16);///%x - TO FIND HEXA DECIMAL VALUE

}
